"""
Assemble the self-contained Claude Code plugin from the platform-neutral core.

Claude Code plugins cannot reference files outside their own root, so the
neutral `core/engine` (runtime) and `core/spec` (procedures/templates) are
*vendored* into the adapter:

    core/engine/*.py (minus *_test.py)  ->  adapters/claude-code/hooks/_engine/
    core/spec/**                        ->  adapters/claude-code/spec/

Run `assemble.py` to (re)generate the vendored trees, or `assemble.py --check`
in CI to fail when they drift from the source (the adapter never hand-edits
the vendored copies).

Stdlib-only; importable (logic in functions, guarded entry block) so pytest can
exercise it directly.
"""

import argparse
import filecmp
import os
import shutil
import sys

REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))

# (source dir, dest dir, predicate(name) -> include?) vendoring jobs.
ENGINE_SRC = os.path.join("core", "engine")
ENGINE_DEST = os.path.join("adapters", "claude-code", "hooks", "_engine")
SPEC_SRC = os.path.join("core", "spec")
SPEC_DEST = os.path.join("adapters", "claude-code", "spec")

VENDOR_HEADER = "# GENERATED by build/assemble.py from core/ — do not edit; run `python3 build/assemble.py`.\n"


def is_runtime_module(name: str) -> bool:
    """Include .py engine modules except their pytest suites and caches."""
    return name.endswith(".py") and not name.endswith("_test.py")


def iter_engine_files(src_abs: str) -> list[str]:
    """Return engine module basenames to vendor (sorted, deterministic)."""
    return sorted(n for n in os.listdir(src_abs) if is_runtime_module(n))


def vendor_engine(repo_root: str, check: bool) -> list[str]:
    """
    Vendor core/engine -> hooks/_engine. Returns a list of drift descriptions
    (empty when in sync). In write mode, makes dest match source.
    """
    src_abs = os.path.join(repo_root, ENGINE_SRC)
    dest_abs = os.path.join(repo_root, ENGINE_DEST)
    files = iter_engine_files(src_abs)
    drift: list[str] = []

    if check:
        if not os.path.isdir(dest_abs):
            return [f"{ENGINE_DEST}/ missing — run assemble"]
        expected = set(files) | {"__init__.py"}
        actual = {n for n in os.listdir(dest_abs) if n.endswith(".py")}
        for stale in sorted(actual - expected):
            drift.append(f"stale vendored module: {ENGINE_DEST}/{stale}")
        for name in files:
            dpath = os.path.join(dest_abs, name)
            if not os.path.exists(dpath):
                drift.append(f"missing vendored module: {ENGINE_DEST}/{name}")
                continue
            if _read(os.path.join(src_abs, name)) != _strip_header(_read(dpath)):
                drift.append(f"out-of-sync vendored module: {ENGINE_DEST}/{name}")
        ipath = os.path.join(dest_abs, "__init__.py")
        if not os.path.exists(ipath):
            drift.append(f"missing {ENGINE_DEST}/__init__.py")
        return drift

    # write mode
    if os.path.isdir(dest_abs):
        shutil.rmtree(dest_abs)
    os.makedirs(dest_abs)
    _write(os.path.join(dest_abs, "__init__.py"), VENDOR_HEADER + '"""Vendored docs-keeper engine (generated)."""\n')
    for name in files:
        _write(os.path.join(dest_abs, name), VENDOR_HEADER + _read(os.path.join(src_abs, name)))
    return drift


def vendor_spec(repo_root: str, check: bool) -> list[str]:
    """
    Vendor core/spec -> adapters/claude-code/spec (recursive tree copy).
    Returns drift descriptions (empty when in sync).
    """
    src_abs = os.path.join(repo_root, SPEC_SRC)
    dest_abs = os.path.join(repo_root, SPEC_DEST)
    drift: list[str] = []

    if check:
        if not os.path.isdir(dest_abs):
            return [f"{SPEC_DEST}/ missing — run assemble"]
        drift.extend(_tree_diff(src_abs, dest_abs, SPEC_DEST))
        return drift

    if os.path.isdir(dest_abs):
        shutil.rmtree(dest_abs)
    shutil.copytree(src_abs, dest_abs)
    return drift


def assemble(repo_root: str = REPO_ROOT, check: bool = False) -> list[str]:
    """Run every vendoring job. Returns combined drift list (empty == in sync)."""
    return vendor_engine(repo_root, check) + vendor_spec(repo_root, check)


# ---------------------------------------------------------------------------
# helpers
# ---------------------------------------------------------------------------


def _read(path: str) -> str:
    with open(path, encoding="utf-8") as fh:
        return fh.read()


def _write(path: str, content: str) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    # newline="\n" keeps vendored output LF-deterministic regardless of host OS.
    with open(path, "w", encoding="utf-8", newline="\n") as fh:
        fh.write(content)


def _strip_header(content: str) -> str:
    """Drop the generated-file header line for content comparison."""
    if content.startswith(VENDOR_HEADER):
        return content[len(VENDOR_HEADER):]
    return content


def _tree_diff(src: str, dest: str, dest_label: str) -> list[str]:
    """Recursively compare two trees; return human-readable drift descriptions."""
    drift: list[str] = []
    cmp = filecmp.dircmp(src, dest)
    for name in sorted(cmp.left_only):
        drift.append(f"missing in {dest_label}: {name}")
    for name in sorted(cmp.right_only):
        drift.append(f"stale in {dest_label}: {name}")
    for name in sorted(cmp.diff_files):
        drift.append(f"out-of-sync in {dest_label}: {name}")
    for sub in sorted(cmp.common_dirs):
        drift.extend(_tree_diff(os.path.join(src, sub), os.path.join(dest, sub), f"{dest_label}/{sub}"))
    return drift


def main() -> None:
    parser = argparse.ArgumentParser(description="Assemble the self-contained docs-keeper Claude Code plugin.")
    parser.add_argument("--check", action="store_true", help="Verify vendored trees are in sync; exit 2 on drift.")
    args = parser.parse_args()

    drift = assemble(check=args.check)
    if args.check:
        if drift:
            print("assemble --check: core/adapter out of sync:", file=sys.stderr)
            for d in drift:
                print(f"  - {d}", file=sys.stderr)
            print("Run: python3 build/assemble.py", file=sys.stderr)
            sys.exit(2)
        print("assemble --check: core <-> adapter in sync.")
        sys.exit(0)

    print("Assembled plugin: vendored core/engine -> hooks/_engine, core/spec -> adapters/claude-code/spec.")
    sys.exit(0)


if __name__ == "__main__":
    main()
